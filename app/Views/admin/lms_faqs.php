								<div class="panel-heading" role="tab" id="headingEight">
									<a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapse10" aria-expanded="false" aria-controls="collapse10" > 10. What  are some of the Dos & Don't of a Competency Process? </a>
								</div>
								<div id="collapse10" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingEight">
									<div class="panel-body pa-15"> 
									<p class="text-muted mb-10">
										Before identifying the Dos and Dont’s of the competency development process, it is necessary to understand that fundamental premise of the entire initiative or exercise; which is the development of the individual.  Having said that, some of the Dos and Dont’s that have to be followed are  
									</p>
									<p class="text-muted mb-10">
										Dos’
										<ul class="uo-list  pl-15">
											<li>Make the process a highly involved one – get people’s buy-in at every stage</li>
											<li>Ensure that communication is constantly and continuously sent out to all the constituents</li> 
											<li>Make the process highly-non threatening</li>
											<li>Ensure proper care in drawing-up and conducting the assessment process – from method of assessment, to choice of assessors to preparing the report</li>
											<li>Emphasise the core purpose of the process, which is usually development</li>
										</ul>	
									</p>
									<p class="text-muted mb-10">
										Don’t’s
										<ul class="uo-list  pl-15">
											<li>Make the process as simple as possible – donot use complex templates and methods</li>
											<li>Use a language that is understandable to all – dejargonize at every stage</li> 
											<li>Do not raise unnecessary fears in people about the process of assessment, by not communicating properly</li>
											<li>Do not get overtly trapped in detailing; don’t miss the woods for the trees.</li>
											<li>Do not use the word assessment, by itself; that is always use assessment in conjunction with development</li>
										</ul>	
									</p>
									</div>
								</div>
