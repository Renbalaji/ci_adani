								</div>
								<div id="collapse16" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingEight">
									<div class="panel-body pa-15"> 
										<p  class="text-muted mb-10">
										Vectorizing is a process of determining/assigning a numeric value to the  gap/strength and arriving at a direction (assigning positive or negative values).  Following are the steps involved in the process of vectorization  
										</p>
										<p  class="text-muted mb-10">
										Step1 :  Identify the gap level in a competency –  for instance required level is developed, assessed level is novice, implies ga is “2” 
										</p>
										<p  class="text-muted mb-10">
										Step 2.  Identify the level of criticality to the competency (assign numeric values to it); Critical = 5, Important =3, Less Important = 1 (we can assign any scale, but we would have to be consistent through out). 
										</p>
										<p  class="text-muted mb-10">
										Step 3.  Multiply the gap level with the criticality dimension
										</p>
										<p  class="text-muted mb-10">
										Step 4.  Vectorize the magnitude  strength/gap (step 3), by a proportionate sign/arrow  ie usually a positive sign for the strength and negative for the gap.  
										</p>
