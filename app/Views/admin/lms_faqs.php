									<a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapse9" aria-expanded="false" aria-controls="collapse9" > 9. What is Competency Based Management? </a>
								</div>
								<div id="collapse9" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingEight">
									<div class="panel-body pa-15"> 
										<p class="text-muted mb-10">
										The Competency Mapping, Assessment and Development Process has multiple usages; some of which include   
										</p>
										<p class="text-muted mb-10">
										(a)	Manpower Planning:  The term people requirement in an organization is actually a misnomer.  What an organization wants is a certain set of skills and capabilities; stated otherwise organizational manpower requirement is synonymous with Organization Competency Requirements.  Hence, the competency information can be used for the process of manpower planning
										</p>
										<p class="text-muted mb-10">
										(b)	Recruitment:  One of the areas  where competency related information can be used is in the process of recruitment.  Since, competency by definition is a combination of knowledge and skills, usage of competency profiles for the selection process enables in identifying the exact requirement of the organization (that is, it helps in preparing the job specification template).  This information in turn helps in selecting the “right fit” .
										</p>
										<p class="text-muted mb-10">
										(c)	Learning & Development:  Most organization use the Competency process for drawing-up individual (as well as group) development road maps.  As opposed to the conventionally followed approach to the training and development need identification process, this methodology helps in drawing-up a more focused and (individually) meaningful development processes.  This learning and development road maps include all the information for individual development
										</p>
										<p class="text-muted mb-10">
										(d)	Performance Management System: The Competency process can also be used in the performance management system.  The incorporation of the competency process as a part of the PMS would depend on a number of organization related issues, which include level of acceptance, the maturity of the system, the synergy with the existing PMS etc.  
										</p>
										<p class="text-muted mb-10">
										(e)	Career & Succession Planning:  Since the competency profiling sheets has all the required information for the various unique positions in the organization, it becomes easy for an organization to identify where the present set of incumbents are (post assessment) and where they may “best fit in”.  
										</p>
										<p class="text-muted mb-10">
										The process of using the Competency Related Information, for the various elements of the HR value chain  (as indicated above) is known as Competency Based Management.   
										</p>
										<p class="text-danger mb-10">
										This question is similar to  “what are the various uses  of the competency mapping, assessment and development process” 
										</p>
