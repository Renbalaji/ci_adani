											<b><u>Issue   </u><b>
											<p  class="text-muted mb-10">It seems to be difficult to establish a exact fit – between the competency to be assessed and the method to be used  </p>
											<p  class="text-muted mb-10">
											One of the ways of addressing this problem is to identify the competencies which may have a significant (relatively speaking) knowledge element, an example of this kind could be strategic thinking.  For these kinds of competencies, it is preferable to use case/caselet, interview or discussion methods.   There are certain competencies, which have a significant skill/behavioral element,  such as emotional stability.   For such competencies, a behavioral instrument may be a ideal method for assessment.   For those competencies which have a combination of a knowledge and skill/behavioral component, it is preferable to use combinatorial approach – ie cases, discussions etc and also behavioral. 
											</p>
										</p>
										<p class="text-muted mb-10">
											<b><u>Issue   </u><b>
											<p  class="text-muted mb-10">There are certain competencies, where an ideal instrument or a technique is not available </p>
											<p  class="text-muted mb-10">
											At times, certain competencies may be included in the basket,  where to identify an exact method may be difficult.  In such cases, it should be ensured during the process of assessment, that either through the process of discussion or interviewing, the person is particularly probed on issues/elements relating to this competency.  Apart from this, using techniques such as 360 degree feedback will help in the process of assessment.  
											</p>
										</p>
										<p class="text-muted mb-10">
											<b><u>Issue   </u><b>
											<p  class="text-muted mb-10">The appropriate case study doesn’t seem to be available</p>
											<p  class="text-muted mb-10">
											Usually for most of the identified competencies, case studies are available (ideal sources are Harvard , Thunderbird , ICFAI , and  the European Case Clearing House ). There are certain points that have to be borne in mind in choosing a case 
											</p>
											<p  class="text-muted mb-10">
											(i)	does the central theme of the case relate to the competency being assessed
											</p>
											<p  class="text-muted mb-10">
											(ii)	 depending on the time availability, the group being assessed and the competencies that are to be evaluated, a case of appropriate size/length should be chosen
											</p>
											<p  class="text-muted mb-10">
											(iii)	preferably the case settings should not be too dis-similar to the ones with which the assessee can relate to. 
											</p>
											<p  class="text-muted mb-10">
											In choosing the case, for the process of assessment it is essential to remember that it is not only the case that is important, but more important are the questions that are to be framed.  If in case, a appropriate case is not found, then a case has to be developed, by the group, taking the help of external facilitators.  
											</p>
										</p>
										<p class="text-muted mb-10">
											<b><u>Issue   </u><b>
											<p  class="text-muted mb-10">
											There is a degree of subjectivity in the process of assessment, especially when it comes to the cases
											</p>
											<p  class="text-muted mb-10">
											Through out the process of assessment, there is bound to be a certain amount of subjectivity, more so when evaluating cases.  With regards cases, on framing questions, it may be beneficial to prepare a set of points which you feel the presented has to include as a part of his analysis.  Apart from this, juxtaposing the results of the case along with information from other cases can also help in the process of getting a composite picture.  
											</p>
										</p>
										<p class="text-muted mb-10">
											<b><u>Issue   </u><b>
											<p  class="text-muted mb-10">
											Two of the instruments are throwing up conflicting results 
											</p>
											<p  class="text-muted mb-10">
											Most of the times, such a situation does not happen, provided the instruments and methods chosen have been done so with great care.  Even in the extreme event that this happens, one may use the following to resolve the issue
											</p>
											<p  class="text-muted mb-10">
											(i)	see for the trend, on that particular competency, by collating information from all the administered instruments and cases.  Usually this should give some kind of indication of the persons competency in that area.  
											</p>
											<p  class="text-muted mb-10">
											(ii)	In the unlikely event that one does not still get a trend, then use information from instruments such as peer group review or 360 degree feedback.    
											</p>
