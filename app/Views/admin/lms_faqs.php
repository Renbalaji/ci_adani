								<div class="panel-heading" role="tab" id="headingEight">
									<a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapse7" aria-expanded="false" aria-controls="collapse7" > 7. What is Competency Assessment ?</a>
								</div>
								<div id="collapse7" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingEight">
									<div class="panel-body pa-15"> 
										<p class="text-muted mb-10">
										The final and the most important step in the process of Competency Development is the assessment stage.  Having identified what competencies are required at what level of proficiency (the information that is captured in the Competency Profile), it becomes necessary map the individual (the present incumbent) with respect to the job profile.  Depending on what competencies are identified, the assessment process – the instruments used, the tools and techniques employed and the methodology followed, would vary.  However, in a broad sense, the following are some of the tools and techniques that often used  
										</p>
										<p class="text-muted mb-10">
										<ul class="uo-list  pl-15">
											<li>Case Analysis and Case Presentations</li>
											<li>Behavioral and Psychometric  Instruments</li> 
											<li>Peer Review and/or 360 Degree Feedback</li>
											<li>Group Discussions</li>
											<li>In- Basket exercises</li>
											<li>Interviews/Interactive Sessions</li>
											<li>Mock events/simulations</li>
											<li>Other specific methods</li>
										</ul>
										</p>
										<p class="text-muted mb-10">
											The assessment process, is one most complex portion of the entire assessment process and has to be handled with at most sensitivity.  A number of questions such as,
										</p>
										<p class="text-muted mb-10">
											The assessment process, is one most complex portion of the entire assessment process and has to be handled with at most sensitivity.  A number of questions such as,
											<ul class="uo-list  pl-15">
												<li>what instruments have to be used?</li>
												<li>do we use the same set of instruments for the entire population/target group?</li> 
												<li>who shall be the assessors?</li>
												<li>can the entire assessment be objective?</li>
												<li>how should the results be presented?</li>
												<li>what is the post assessment feedback session?</li>
											</ul>
										</p>
										<p class="text-muted mb-10">
										are some of the issues that often crop-up.  These and other related matters have been discussed in a separate document.  
										</p>
									</div>
								</div>
