									<a role="button" data-toggle="collapse" href="#collapseFive" aria-expanded="true" aria-controls="collapseFive" >1. What are competencies?</a> 
								</div>
								<div id="collapseFive" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingFive">
									<div class="panel-body pa-15">
									<p class="text-muted mb-10">The word Competence has been defined by a number of people in varied ways.  Not only is the definition a point of contention amongst various competency theorists, but also as to what constitutes a competency has been hotly debated.</p>
									<p class="text-muted mb-10">
									For the purpose of this program, we shall define competency as a combination of attitude,  skills and knowledge which leads to excellence in performance, or performance which can distinguish between average-performers and superior performers.  The key words here are
									</p>
									<div class="col-lg-9 col-md-6 col-sm-6 col-xs-12">
										<p class="text-muted mb-10">
										(i)	attitude: it is defined as a combination of traits and behavior: and is representative of the general disposition of the individual to the process of learning and application of knowledge
										</p>
										<p class="text-muted mb-10">
										(ii) skill: is defined as the practiced ability, or is the demonstrated ability of carrying out a particular role 
										</p>
										<p class="text-muted mb-10">
										(iii) knowledge: in the context of competency definition it refers to the facts, procedures, and information required to perform a role 
										</p>
									</div>
									<div class="col-lg-3 col-md-6 col-sm-6 col-xs-12">
										<img class="img-responsive" src="<?php echo BASE_URL;?>/public/faq/faq1.png">
									</div>
									<p class="text-muted mb-10">
									Please note, that often the words competence and competency are used as if they are synonymous.  There is however difference, both from the perspective of the language as well as usage.  From the language perspective  - Competence may be defined as ‘demonstrating the knowledge, skills, experience, and attributes necessary to carry out a defined function effectively.’  While  Competency may be defined as ‘the description of the knowledge, skills, experience and attributes necessary to carry out a defined function effectively.’  And from the usage angle, competent; when used in normal parlance it refers to someone who can ‘just about manage’.  However, the opposite of the word, i.e. incompetent or incompetence, when used, can have large negative implications.
									</p>
									</div>
								</div>
							</div>
							<div class="panel panel-default">
								<div class="panel-heading" role="tab" id="headingSix">
									<a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseSix" aria-expanded="false" aria-controls="collapseSix" >2. Are there different sorts of competencies? How important are all of them?</a>
								</div>
								<div id="collapseSix" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingSix">
									<div class="panel-body pa-15"> 
										<p class="text-muted mb-10">
										The competency model for an individual position is normally derived from three different types of competencies; functional, managerial and behavioural.  This is not a fixed rule; based on the model, the managerial and behavioural competencies can be grouped together; leadership competencies can be added as a different cluster.   We shall however be discussing only the managerial and behavioural competencies for this study/program.
										</p>
										<p class="text-muted mb-10">
											<span class="address-head mb-5">1. Managerial Competencies</span>
											<br>
											These are those competencies (knowledge and skill) which are required for effectively performing a managerial role.  Some of the most commonly required managerial competencies are time management, customer focus, negotiation skills, planning, strategic thinking, leadership etc.  
										</p>
										<p class="text-muted mb-10">
											<span class="address-head mb-5">2. Behavioural Competencies </span>
											<br>
											As mentioned, distinction between a managerial and behavioural competency is quite thin.   As a rough heuristic it can be taken that, when the skill element  of the competency is more significant than the knowledge element of the competency, then we can (assume, without gross error), that the competency is a behavioural competency.  Therefore, it is quite common to see that many models list communication as a behavioural competency.  
										</p>
										<p class="text-muted mb-10">
											<span class="address-head mb-5">3. Functional Competencies</span>
											<br>
											The functional competencies are those set of knowledge and skills that are required for performing a job or a role.  These competencies are usually gained through formal ( and at times professional) education.   For instance, Manpower planning is a functional competency required for a HR professional.  Like wise, Risk Management is a functional competency required for a Manager Risk.   
										</p>
										<p class="text-muted mb-10">
											Coming to the next part of the question, the relative importance of each of these competencies (ie managerial, behavioural and functional) is dependent on the position.  The most commonly understood and accepted principle is that, with increasing responsibility (that is upward movement in the organizational hierarchy), the relative importance of functional competencies decreases, while that of the behavioural increases.  It is in fact, this is one of the reasons, that some of the companies take up a separate “level” of competencies to address leadership related issues.  
										</p>
									</div>
								</div>
							</div>
							<div class="panel panel-default">
								<div class="panel-heading" role="tab" id="headingSeven">
									<a class="collapsed" role="button" data-toggle="collapse"  href="#collapseSeven" aria-expanded="false" aria-controls="collapseSeven" >3. How do you identify competencies that are required for an organization?</a>
								</div>
								<div id="collapseSeven" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingSeven">
									<div class="panel-body pa-15"> 
									<p class="text-muted mb-10">
										The process of identifying competencies for an organization is a very important step in the entire process.  To briefly state, every organization has a certain Vision and Mission.  This is usually drawn-up in the context of the present and future business dynamics (the business space in which it operates).  Based on the Vision, certain objectives are set, at the organizational and also the divisional levels (assuming that the organization has multiple divisions).  And for achieving these objectives, organizations draw up, what are known as strategies.  Now, the strategies are further drilled down to functional levels – and these are known as functional strategies.  
									</p>
									<p class="text-muted mb-10">
										Based on the objectives for the organization, the divisional and then the functional objectives are derived.  These are done in such a way that they all dove tail together so as to enable the achievement of the organizational vision and mission.    
									</p>
									<p class="text-muted mb-10">
										The functional objectives, in each of the functions are drilled down further to the individual level, to form what are known as KRA or Key Result Areas.  So an individual who is manning a position would know through his job description of which the Key Result Area is a component, what is expected of him or her.  And, in order for that individual to achieve those KRA’s  he/she would require to posses certain knowledge and skills, or otherwise known as competencies.   A sum total of such competency requirements for various positions in the organization will help one to arrive at the competency basket or the enterprise wide competency requirement.   
									</p>
									<p class="text-muted mb-10">
										But this begs one question to be answered – if the competencies are drawn based on the KRA of various positions (and therefore the immediate needs), how does one take into consideration the future requirements?  The future requirements of the organizations can be got through a group process of the Senior Management Team.  There are also other methods of getting to the future  (and even the present requirements) of competencies; one of which is through Managerial & Behavioural  questionnaire.  This is an instrument which takes an “almost 360 degree” feedback on the competency requirements for various unique positions in the organization; information from which is then collated.
									</p>
									
									</div>
								</div>
							</div>
							<div class="panel panel-default">
								<div class="panel-heading" role="tab" id="headingEight">
									<a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseEight" aria-expanded="false" aria-controls="collapseEight" > 4. How many competencies are required to be identified? </a>
								</div>
								<div id="collapseEight" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingEight">
									<div class="panel-body pa-15"> 
										<p class="text-muted mb-10">
										More than answering the question of how many competencies are required for the mapping and development it will be important to answer the following before finalizing the competency basket
										</p>
										<p class="text-muted mb-10">
										(a)	Do the identified competencies take care of the need of the business, both from the present as well as future needs?  For example, it may be possible that at present the organization may not have a need for ensuring an elaborate knowledge management process, but this may be necessary at a later date.  Then it would be prudent to include it, subject to other conditions (listed below)
										</p>
										<p class="text-muted mb-10">
										(b)	Are the identified competencies distinctive enough?  This means that the competencies have to be identified in such a way that there is a clear distinction between those identified.  For instance it may not serve much purpose to have presentation skills and communication skills as two different ones, until and unless there is a real hard case for the same.
										</p>
										<p class="text-muted mb-10">
										(c)	Do the identified set of competencies cover all the organizational requirements?  As mentioned there are a number of unique positions in the organization and the competency basket identified should ensure that the requirements of all these positions are covered/taken care off. 
										</p>
										<p class="text-muted mb-10">
										(d)	Does the identified list consists of competencies and not activities?  The definition of a competency, as mentioned earlier is a combination of knowledge and skill; so any identified competency should have both these elements – the degree and extent of each of the components can vary.  So, when identifying a competency it should borne in mind the purpose is eventually to develop that (in an individual), and if the competency so chosen, that this purpose cannot be achieved, then the objective will be defeated.  
										</p>
										<p class="text-muted mb-10">
										(e)	Finally, is the list manageable?  A lot of times, the tendency would be to have a large number of competencies.  But again, it has to be borne in mind that, we have to assess and develop these competencies, and hence a list which is both manageable and tractable should be chosen.
										</p>
										<p class="text-muted mb-10">
										It is important that when competency basket is identified these questions are answered by the group.  
										</p>
									</div>
								</div>
							</div>
							<div class="panel panel-default">
								<div class="panel-heading" role="tab" id="headingEight">
									<a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapse5" aria-expanded="false" aria-controls="collapse5" > 5. What is a Competency Dictionary  ?</a>
								</div>
								<div id="collapse5" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingEight">
									<div class="panel-body pa-15">
										<p class="text-muted mb-10">
										Usually, most of the identified competencies, be it managerial or behavioural will be terms/words/phrases which forma part of common management/organizational vocabulary, for eg. communication.    This leads us to a problem, that is, since it is a part of the general diction of the organization, every one thinks they understand the term (eg. Communication), but there will not be a common understanding of the word.  That is, continuing with our example of communication, for some it may mean (excellent ) speaking, for some others it may mean ability to make presentation, and to still others ability to make good reports, etc. Though all of this does constitute communication, it is (a) not all of it, and (ii) it is not the same for every one.  In order to overcome this problem/issue, a Competency Dictionary is prepared.   
										</p>
										<p class="text-muted mb-10">
										“A COMPETENCY DICTIONARY IS A ENTERPRISE/ORGANIZATION SPECIFIC DOCUMENT PREPARED IN CONSULTATION WITH ALL KEY CONSTITUENTS WHICH CLEARLY AND LUCIDLY DEFINES AND EXPLAINS, IN TERMS MEANING, OBSERVABLE/DEMONSTRATE SKILLS, KNOWLEDGE REQUIREMENTS OF  THE VARIOUS FUNCTIONAL/ MANAGERIAL /BEHAVIOURAL COMPETENCIES IDENTIFIED BY THE ORGANIZATION”
										</p>
										<p class="text-muted mb-10">
										A competency dictionary is a document which helps one along all the elements of the Competency Mapping, Assessment and Development Process.  By establishing common definitions, it helps in ensuring that all the constituents have the same understanding of the competency.  Since all the elements are clearly defined, the process of assessment is facilitated, since the aspect of ‘what needs to be assessed’ is well established.  The process of development, is likewise facilitated by the dictionary, as clear issues of what needs to be focused upon by an individual can easily be established.
										</p>
										<p class="text-muted mb-10">
										A competency dictionary is not a static document.  One, with changing strategies, new competencies get added to the basket. Two, also with changing conditions, new definitions of existing competencies may have to be drawn-up.  
										</p>
										
									</div>
								</div>
							</div>
							<div class="panel panel-default">
								<div class="panel-heading" role="tab" id="headingEight">
									<a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapse6" aria-expanded="false" aria-controls="collapse6" > 6. What is Competency Profiling ?  </a>
								</div>
								<div id="collapse6" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingEight">
									<div class="panel-body pa-15">
										<p class="text-muted mb-10">
										Positions in the organization structure, depending on nature and volume  are classified as unique and repetitive.  Unique positions, as the name indicates are “one-of-the-kind” positions;  organizations would therefore have positions within the structure such that the total number of people are equal to or greater than the number of unique positions.  To illustrate, there may be one Risk Manager; while there may be three accountants, in an organization, all doing the same job.  So the number of unique positions in this case would be two, while the total number of positions are 4 (three accountants and one manager).  
										</p>
										<p class="text-muted mb-10">
										Since, the job done by each of the accountants is the same, the organization has, what is known as one unique Job Description*.  For each of these unique positions, the competency required is mapped based on the KRAs.   The mapping of the competencies, for each of the unique positions, is known as Competency Profiling of unique positions.  The following are the steps to be followed for competency profiling  
										</p>
										<p class="text-muted mb-10">
										(a)	Identify all the unique positions in the organization.  If you are in doubt about classifying any position whether it is unique or not, err on the side of unique, ie assume that the position is unique. 
										</p>
										<p class="text-muted mb-10">
										(b)	For each unique position, identify the level of criticality of the identified competencies.  For the purpose of this study/program we have three levels of criticality
										</p>
										<p class="text-muted mb-10">
										<b>Critical</b>:  Are those competencies which are vital for successfully performing the job/role, absence of which (competencies) directly impacts the results.  These are competencies in which a gap, if present, has to be bridged on (top) priority
										</p>
										<p class="text-muted mb-10">
										<b>Important</b>:These are those competencies which are necessary for carrying out the job; but may not directly impact immediate performance.  Gaps should be bridged
										</p>
										<p class="text-muted mb-10">
										<b>Less Important</b>: These are those competencies which may not directly impact the performance of the job, but the knowledge of which could be beneficial – it can be viewed as a Add on Competency.  Gaps can be bridged based on the organizational need
										</p>
										<p class="text-muted mb-10">
										(c)	Having identified the level of criticality, next the level of proficiency has to be determined.  It has to be noted that the level of criticality to proficiency is NOT a one-to-one relationship,  which means that for an unique position, a given competency may be Critical, but the level of proficiency may be Practitioner; whereas for another position the competency may be critical and the level of requirement (proficiency) may be Developed.  
										</p>
										<p class="text-muted mb-10">
										It is preferable to profile positions in a group.   The group should preferably consists of some senior member along with HR team (apart from the members of that department/function)  to ensure that the process is carried out objectively.  
										</p>
										<p class="text-danger mb-10">
										* A job description is a document which lists all the roles and responsibilities, accountabilities, expected results (KRA) of a job  
										</p>
									</div>
								</div>
