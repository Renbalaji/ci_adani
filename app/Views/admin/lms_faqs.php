							<div class="panel-heading">
								<div class="pull-left">
									<h6 class="panel-title txt-dark">Developing Competency Dictionary  </h6>
								</div>
								<div class="clearfix"></div>
							</div>
							<div class="panel panel-default">
								<div class="panel-heading" role="tab" id="headingEight">
									<a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapse11" aria-expanded="false" aria-controls="collapse11" > 1. What is a Competency Profiling? Why do we need to Profile Positions?</a>
								</div>
								<div id="collapse11" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingEight">
									<div class="panel-body pa-15"> 
										<p class="text-muted mb-10">
											Positions in the organization structure, depending on nature and volume  are classified as unique and repetitive.  Unique positions, as the name indicates are “one-of-the-kind” positions;  organizations would therefore have positions within the structure such that the total number of people are equal to or greater than the number of unique positions.  To illustrate, there may be one Risk Manager; while there may be three accountants, in an organization, all doing the same job.  So the number of unique positions in this case would be two, while the total number of positions are 4 (three accountants and one manager).  
										</p>
										<p class="text-muted mb-10">
											Since, the job done by each of the accountants is the same, the organization has, what is known as one unique Job Description.  For each of these unique positions, the competency required is mapped based on the KRAs.   <u>The mapping of the competencies, for each of the unique positions, in terms of criticality and proficiency (refer next question)  is known as Competency Profiling of unique positions.</u> 
										</p>
										<p class="text-muted mb-10">
											 Profiling of positions helps the organization in many ways 
										</p>
										<p class="text-muted mb-10">
											(a)	Not all competencies, as mentioned, are important for a job, to the same degree.  The process of profiling will help in focusing of energies by the organization and also the individual manning the position, on those areas which make a significant impact on the performance. 
										</p>
										<p class="text-muted mb-10">
											(b)	The organization, can use the information from the competency profile for succession planning, i.e. it will make the process of identifying, among the various reporting positions, a suitable reporting position which can eventually be ‘moved-up’.  
										</p>
										<p class="text-muted mb-10">
											(c)	Apart from this, the competency profiling process helps, in areas such as in recruitment and training.    
										</p>
									</div>
								</div>
							</div>
							<div class="panel panel-default">
								<div class="panel-heading" role="tab" id="headingEight">
									<a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapse12" aria-expanded="false" aria-controls="collapse12" > 2. How is Competency Profiling done?</a>
								</div>
								<div id="collapse12" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingEight">
									<div class="panel-body pa-15"> 
										<p class="text-muted mb-10">
											Competency profiling is usually a group process; and consists of people belonging to that particular function/department are area and representatives from HR, along with facilitating/consulting agency (based on need/organization requirement).  The people from the function are so chosen that there is at least one representative person for each of the unique positions identified (at times it may be convenient to have more than one person representing a unique position).   
										</p>
										<p class="text-muted mb-10">
											Once the group has been formed (and assembled) the following are the steps that are followed  
										</p>
										<p class="text-muted mb-10">
											(a)	Reconfirmation of the unique positions in the department.  If in doubt about classifying any position whether it is unique or not, err on the side of unique, ie assume that the position is unique. 
										</p>
										<p class="text-muted mb-10">
											(b)	For each unique position, identify the level of criticality of the identified competencies.  For the purpose of this study/program we have three levels of criticality
											<p class="text-muted mb-10">
												<b>Critical:</b>  Are those competencies which are vital for successfully performing the job/role, absence of which (competencies) directly impacts the results.  These are competencies in which a gap, if present, has to be bridged on (top) priority
											</p>
											<p class="text-muted mb-10">
												<b>Important :</b> These are those competencies which are necessary for carrying out the job; but may not directly impact immediate performance.  Gaps should be bridged
											</p>
											<p class="text-muted mb-10">
												<b>Less Important :</b> These are those competencies which may not directly impact the performance of the job, but the knowledge of which could be beneficial – it can be viewed as a Add on Competency.  Gaps can be bridged based on the organizational need
											</p>
										</p>
										<p class="text-muted mb-10">
											(c)	Having identified the level of criticality, next the level of proficiency has to be determined.  It has to be noted that the level of criticality to proficiency is NOT a one-to-one relationship,  which means that for an unique position, a given competency may be Critical, but the level of proficiency may be Practitioner; whereas for another position the competency may be critical and the level of requirement (proficiency) may be Developed.  
										</p>
										<p class="text-muted mb-10">
											After completion  of the profiling for a entire department/function, prepare a composite statement of the profile information of the various unique positions.  This will help in establishing a quick reference of requirements, in a given department or function.   
										</p>
									</div>
								</div>
							</div>
							<div class="panel panel-default">
								<div class="panel-heading" role="tab" id="headingEight">
									<a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapse13" aria-expanded="false" aria-controls="collapse13" > 3. What may be some of the issues that come-up in profiling? </a>
								</div>
								<div id="collapse13" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingEight">
									<div class="panel-body pa-15">
										<p class="text-muted mb-10">
											The following may be some of the issues that come-up in the process of profiling  
										</p>
										<p class="text-muted mb-10">
											<b><u>Issue</u><b><br>
											The information in the JD is not very clear to enable the process of profiling.The, information in a job description is usually in a nartive/descriptive form and the only 	quantitative data (relevant to profiling)  that is available is in the form of KRAs. When looking/analysing a JD, one should pay careful attention to the key aspects that are 			expected to be performed by that job-holder. In case, even after careful scrutiny the profiling related information cannot be culled, then information from other inputs, such as Managerial and Behavioural sheets should be used.Apart from this,the issue can partly be addressed by doing it in a group. 
										</p>
										<p class="text-muted mb-10">
											<b><u>Issues </u><b><br>
											The JD is not available
											Profiling can be done, only on the availability of a JD.In case no formal JD is available, have a draft form of it atleast prepared, before commencing the profiling process.

										</p>
										<p class="text-muted mb-10">
											<b><u>Issue  </u><b><br>
											There is no agreement, between the incumbent, his boss, and HR on the level of criticality and level of proficiency for a particular position.The disagreement usually comes because of a generally tendency for any incumbent to look at the requirements for the position he/she is manning, to be much higher than what may be required.  This comes from two reasons, one a person wanting to know that his/her job requirements are very stringent/high and two, because of some kind of a comparison, with an equivalent job elsewhere in the organization. In rare instances, it may also be possible, that an incumbent would like to look at his/her job, as needing a “lesser” than required level of competencies.Irrespective of the difference (wanting it be high or low), the way to overcome the problem, is make an objective evaluation, using JD information, description of the competency as given in the dictionary, Managerial & behavioural Attribute Sheet,and the group opinion. 
										</p>
									</div>
								</div>
							</div>
							<div class="panel panel-default">
								<div class="panel-heading" role="tab" id="headingEight">
									<a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapse14" aria-expanded="false" aria-controls="collapse14" > 4. What is Competency Assessment? How is it done and what are the steps involved in this? </a>
								</div>
								<div id="collapse14" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingEight">
									<div class="panel-body pa-15">
										<p class="text-muted mb-10">
											The most important step in the process of Competency Development is the assessment stage.  Having identified what competencies are required at what level of proficiency (the information that is captured in the Competency Profile), it becomes necessary to map the individual (the present incumbent) with respect to the job profile.  
										</p>
										<p class="text-muted mb-10">
											The following are the steps involved in the process of competency assessment 
										</p>
										<p class="text-muted mb-10">
											(a)	First and foremost, in the assessment process is identification of the techniques to be used for process of assessment.  This is critical aspect of the whole process.
										</p>
 										<p class="text-muted mb-10">	
											(b)	Having identified the tools, the next step would be to identify the assessors for the process, that is the panel which will be involved in carrying out the assessment
										</p>
										<p class="text-muted mb-10">	
											(c)	Then comes the actual process of assessment itself, which has to carefully orchestrated in terms of environment /ambiance  where it is carried out and also logistics involved.  This could be a important consideration, if the number of people to be assessed are significant. 
										</p>
										<p class="text-muted mb-10">	
											(d)	Preparing an appropriate report for presenting the assessment results, and
										<p class="text-muted mb-10">	
											(e)	Finally a feedback session with the assessee to clarify any issues that he/she may have in the assessment report.  

										</p>
									</div>
								</div>
							</div>
							<div class="panel panel-default">
								<div class="panel-heading" role="tab" id="headingEight">
									<a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapse15" aria-expanded="false" aria-controls="collapse15" > 5. What are some of the common issues involved in the process of assessment?</a>
								</div>
								<div id="collapse15" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingEight">
									<div class="panel-body pa-15"> 
										<p class="text-muted mb-10">	
											The following are some of the issues that may come-up prior to or during the process of assessment
										</p>
										<p class="text-muted mb-10">
											<b><u>Issue   </u><b><br>
											There is a difficulty in identifying the assessment tools/techniques Before identifying the techniques, it would be necessary to determine the amount of time that the organization will be spending in the process of assessment.  If the number of people to be assessed are quite large it will not be possible to deploy a very intensive approach (For example, if the total number of people to be assessed are say 600, then using a method which takes about 2-3days for assessment, would mean a total of 1200-1800 man-days for this process, which the organization will have to ear-mark!)  Once the size of the group to be assessed and the probable time allocated are determined, then the process of identifying the method/tools/techniques to be deployed can be started.  When identifying the tools and techniques, the first and foremost rule would be to ensure that for each of the identified competencies, we can get more than one (i.e. multiple) opportunity for assessing the person on that competency.  Simply put, if a matrix of competencies and the techniques is prepared, as shown below, the most densely populated matrix would be the most effective one. 

