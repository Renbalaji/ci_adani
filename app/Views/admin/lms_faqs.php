							<div class="panel panel-default">
								<div class="panel-heading" role="tab" id="headingEight">
									<a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapse8" aria-expanded="false" aria-controls="collapse8" > 8. What is Competency Assessment Report?</a>
								</div>
								<div id="collapse8" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingEight">
									<div class="panel-body pa-15">
										<p class="text-muted mb-10">
										In the process of competency assessment, one of the important steps is the preparation of the Assessment Report.  Once the assessment process is completed, the individual will have to be provided with a feedback on the assessment.  This feedback is usually in the form of a written report (in some extreme cases, if agreed upon initially/or at the start of the process, it can be in a oral form).  This feedback report, known as the Competency Assessment Report, usually contains the following 
										</p>
										<p class="text-muted mb-10">
										(a)	a brief summary of the assessment result of each of the identified competency.  This form of presentation is what many organizations do, while some may take a composite approach method that is giving overall summary.  However, in order to derive maximum benefit out of the process, it is necessary to present results corresponding to each of the competency.  Depending on the scale used, appropriate adjectives are used to describe the individuals ‘demonstrated capability’ on each of the competency 
										</p>
										<p class="text-muted mb-10">
										(b)	profile and assessed results:  the competency profile describes the requirement of the position and after the assessment, it is necessary to present the results with reference to the positional requirements.  This is done, since not all the competencies will be important for a position, and hence viewing gaps or strengths has to be done vis-à-vis the requirements of the position (and not  in isolation )
										</p>
										<p class="text-muted mb-10">
										(c)	  significant strengths and development areas:  as mentioned, multiple instruments/methods are used in the assessment process, and therefore it is relatively easy to identify as to what are an individuals significant strengths (reflected often through the instrument/method) and development areas.
										</p>
										<p class="text-muted mb-10">
										(d)	Vectorized values:  this is a technique where in numbers are used to quantify the requirements of a job and like-wise the assessed results of the individual; and this is done by assigning appropriate values to the levels of proficiency and criticality of each competency.  
										</p>
										<p class="text-muted mb-10">
										Apart from the above, depending on the need of the organization, other information, such as behavioural profile, tests results, case analysis etc. can be included.    
										</p>
									</div>
								</div>
